/** All sliders / selectors that drive the cost model */
interface ModelParams {
  // Fleet
  fleetSize:       number;   // number of missions (top-N by accessibility)

  // Launch economics
  launchCostPerKg: number;   // $/kg to LEO

  // Spacecraft
  propulsion:      PropulsionType;
  dryMassKg:       number;   // spacecraft dry mass [kg]  (structure + propulsion + mining gear)
  cargoCapKg:      number;   // max resource mass per return trip [kg]

  // Operations
  opsPerDay:       number;   // $/day of active mission ops
  maxSurfaceDays:  number;   // hard upper bound on surface time (spacecraft endurance: power, consumables)

  // Program-level
  devCostM:        number;   // total spacecraft development cost [$M], amortized over fleet

  // Mining
  extractionEff:   number;   // fraction of available resource actually extracted (0–1)
  returnFactor:    number;   // return ΔV as a fraction of outbound ΔV (0 = full aerobraking, 1 = symmetric propulsive)
  contingency:     number;   // multiplier on computed ΔV (1.0 = no margin, 1.3 = 30% margin)

  // Per-family physical properties: [primitive, stony, metal-rich, unknown]
  densities:       [number, number, number, number];   // bulk density [kg/m³]
  metalFracs:      [number, number, number, number];   // metal resource mass fraction
}

