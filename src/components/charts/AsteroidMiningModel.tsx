/**
 * AsteroidMiningModel.tsx
 * =======================
 * Interactive first-principles cost model for asteroid mining.
 * Computes $/kg of resource delivered to LEO as a function of
 * mission architecture and physical assumptions.
 *
 * Sections
 * --------
 *  1. Physical constants and propulsion presets
 *  2. Earth mining benchmarks (for comparison)
 *  3. Types and default parameter values
 *  4. Pure computation functions (no React)
 *      - computeMission()       per-asteroid mission cost + yield
 *      - computeFleetCurve()    $/kg vs fleet-size curve (N = 1..MAX_N)
 *  5. React component
 *      - State: model parameters (all sliders)
 *      - useMemo: delta-v sorted asteroid list (expensive, runs once)
 *      - useMemo: fleet curve (runs on every param change)
 *      - useEffect: renders Observable Plot charts
 *      - Render: stats, benchmark bar, slider panel, charts
 *
 * Data source
 * -----------
 * /thoughts/asteroid-mining/asteroid-data.json
 * Generated by scripts/build_website_data.py from JPL SBDB + Lowell AstOrb.
 * Schema: [a_au, e, i_deg, vol_m3, fam, dv_out_km_s, t_transfer_days]
 * Sorted ascending by dv_out_km_s (most accessible first).
 *
 * Assumptions (all exposed as sliders or noted in tech deep-dive):
 *  - Two-stage rocket equation: return propellant launched from Earth
 *  - Cargo loaded at asteroid (not launched from Earth)
 *  - Rendezvous at asteroid perihelion (lower bound on real delta-v)
 *  - No gravity assists; no ISRU propellant production
 *  - Plane change at aphelion of transfer orbit (single impulsive burn)
 */

