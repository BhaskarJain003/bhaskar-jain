/**
 * Compute all cost and yield figures for a single mission to one asteroid.
 *
 * Rocket equation (two-stage, because cargo is loaded at the asteroid):
 *
 *   m_prop_ret = (m_dry + m_cargo) × (exp(ΔV_ret / v_e) − 1)
 *   m_prop_out = (m_dry + m_prop_ret) × (exp(ΔV_out / v_e) − 1)
 *   m_wet      = m_dry + m_prop_out + m_prop_ret
 *
 * This correctly accounts for the outbound leg needing to carry the return
 * propellant (since all propellant is launched from Earth).
 */
function computeMission(
  asteroid: AsteroidRow,
  params: ModelParams,
): MissionResult {
  const {
    launchCostPerKg, propulsion, dryMassKg, cargoCapKg,
    opsPerDay, maxSurfaceDays, devCostM, fleetSize,
    extractionEff, returnFactor, contingency,
    densities, metalFracs,
  } = params;

  const [, , , vol_m3, fam, dv_out_raw, t_transfer] = asteroid;

  const ve = PROPULSION[propulsion].ve;

  // Apply contingency margin to delta-v
  const dv_out = dv_out_raw * contingency;
  const dv_ret = dv_out * returnFactor;

  // SEP missions spiral in/out: trip time is much longer than Hohmann
  const timeMultiplier = propulsion === 'sep' ? SEP_TIME_MULTIPLIER : 1.0;

  // ── Resource yield ──────────────────────────────────────────────────────
  //
  // Optimal surface time: mine until the cargo hold is full, then leave.
  // Staying longer adds ops cost with no extra cargo, so it is never optimal.
  // Two physical constraints cap the time before cargo is full:
  //   1. The asteroid may be too small to fill the hold at all.
  //   2. The spacecraft endurance (power, consumables) sets a hard ceiling.
  //
  //   grade [kg/m³]      = bulk density × resource mass fraction
  //   rateKgPerDay       = MINING_RATE_M3_PER_DAY × grade × extractionEff
  //   daysToFillCargo    = cargoCapKg / rateKgPerDay
  //   daysToMineAsteroid = vol_m3 / MINING_RATE_M3_PER_DAY
  //   surfaceDays        = min(daysToFillCargo, daysToMineAsteroid, maxSurfaceDays)
  //
  const density  = densities[fam];
  const fraction = metalFracs[fam];
  const grade    = density * fraction;   // kg of resource per m³ of bulk material
  const extractionRateKgPerDay = MINING_RATE_M3_PER_DAY * grade * extractionEff;
  const daysToFillCargo    = extractionRateKgPerDay > 0
    ? cargoCapKg / extractionRateKgPerDay
    : maxSurfaceDays;
  const daysToMineAsteroid = vol_m3 / MINING_RATE_M3_PER_DAY;
  const surfaceDays        = Math.min(daysToFillCargo, daysToMineAsteroid, maxSurfaceDays);

  const volumeProcessedM3 = Math.min(vol_m3, MINING_RATE_M3_PER_DAY * surfaceDays);
  const extractedKg  = volumeProcessedM3 * grade * extractionEff;
  const actualCargo  = Math.min(cargoCapKg, extractedKg);   // cargo-cap limited
  // Return / delivery efficiency is assumed 100% — no precedent exists for bulk
  // deep-space return losses, so we do not model a loss factor here.
  const deliveredKg  = actualCargo;

  const missionDays = 2 * t_transfer * timeMultiplier + surfaceDays;

  // ── Two-stage rocket equation ───────────────────────────────────────────
  //  Return leg first (loaded at asteroid, before departure):
  const m_prop_ret = (dryMassKg + actualCargo) * (Math.exp(dv_ret / ve) - 1);
  //  Outbound leg (must carry m_prop_ret from Earth):
  const m_prop_out = (dryMassKg + m_prop_ret)  * (Math.exp(dv_out / ve) - 1);
  const wetMassKg  = dryMassKg + m_prop_ret + m_prop_out;

  // ── Cost breakdown ──────────────────────────────────────────────────────
  const launchCost   = wetMassKg * launchCostPerKg;
  const opsCost      = opsPerDay * missionDays;
  const devCostShare = (devCostM * 1e6) / Math.max(1, fleetSize);
  const totalCost    = launchCost + opsCost + devCostShare;

  return {
    launchCost, opsCost, devCostShare, totalCost,
    deliveredKg, missionDays, surfaceDays, wetMassKg,
    dvTotalKmS: dv_out + dv_ret,
  };
}

/**
 * Compute the cumulative $/kg vs fleet-size curve.
 * Picks the top N asteroids (pre-sorted ascending by dv_out) and
 * accumulates cost and delivered mass.
 *
 * Only points 1..MAX_N are computed to keep the UI responsive.
 */
const MAX_CURVE_N = 200;

function computeFleetCurve(
  sortedAsteroids: AsteroidRow[],
  params: ModelParams,
): CurvePoint[] {
  const curve: CurvePoint[] = [];
  let totalMissionCost = 0;   // launch + ops only (variable costs)
  let totalDelivered   = 0;
  // Dev cost is a one-time fixed programme cost, paid once regardless of fleet
  // size and amortised across all missions.  It must NOT be multiplied by n
  // in the loop — otherwise changing the fleet-size slider reprices every point
  // on the curve and the axis rescales.
  const devCostTotal = params.devCostM * 1e6;
  const limit = Math.min(sortedAsteroids.length, MAX_CURVE_N);

  for (let n = 1; n <= limit; n++) {
    const res = computeMission(sortedAsteroids[n - 1], params);
    // Accumulate only the variable per-mission costs; add devCostTotal once below.
    totalMissionCost += res.launchCost + res.opsCost;
    totalDelivered   += res.deliveredKg;
    const totalCost = totalMissionCost + devCostTotal;
    curve.push({
      n,
      costPerKg:      totalDelivered > 0 ? totalCost / totalDelivered : Infinity,
      totalCost,
      totalDelivered,
    });
  }
  return curve;
}

