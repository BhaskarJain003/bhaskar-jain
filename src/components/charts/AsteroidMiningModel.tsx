/** Result of a single-mission calculation */
interface MissionResult {
  launchCost:    number;   // $
  opsCost:       number;   // $
  devCostShare:  number;   // $
  totalCost:     number;   // $
  deliveredKg:   number;   // kg
  missionDays:   number;   // total days (transit + surface)
  surfaceDays:   number;   // actual mining time used (≤ maxSurfaceDays)
  wetMassKg:     number;   // launch mass [kg]
  dvTotalKmS:    number;   // total ΔV applied [km/s]
}

/** One point on the $/kg vs fleet-size curve */
interface CurvePoint {
  n:           number;   // fleet size
  costPerKg:   number;   // cumulative $/kg
  totalCost:   number;   // cumulative $
  totalDelivered: number;  // cumulative kg
}

